class Solution
{
    public:
    //Function to delete middle element of a stack.
    void deleteMid(stack<int>&s, int sizeOfStack)
    {
        vector<int> temp;   //for storing elements
        int n=sizeOfStack/2;
        while(n--)
        {
            temp.push_back(s.top());
            s.pop();
        }
        s.pop();    //ye middle element hoga,jo humne pop kar diya
        for(int i=temp.size()-1;i>=0;i--)
        {
            s.push(temp[i]);
        }
    }
};

